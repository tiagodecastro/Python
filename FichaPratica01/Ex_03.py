# Ler altura
print("Introduza a altura:")
altura = int(input())

# Ler comprimento
print("Introduza o comprimento:")
comprimento = int(input())

# Calcular área
area = comprimento * altura
print("Área: ", area)

# Calcular perímetro
perimetro = (comprimento * 2) + (altura * 2)
print("Perímetro :", perimetro)