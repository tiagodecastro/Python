# Ler num1
print("Introduza o primeiro número:")
num1 = float(input())
medianum1 = num1 * 0.2

# Ler num2
print("Introduza o segundo número:")
num2 = float(input())
medianum2 = num2 * 0.3

# Ler num3
print("Introduza o terceiro número:")
num3 = float(input())
medianum3 = num3 * 0.5

# Média Aritmética
mediaaritmetica = (num1 + num2 + num3) / 3
print("A média aritmética é: ", mediaaritmetica)

# Média Ponderada
mediaponderada = medianum1 + medianum2 + medianum3
print("A média ponderada é :", mediaponderada)